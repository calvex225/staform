     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'nom' => 'required|string',
        ]);

        //Creer un Utilisateur
        $siteType = new SiteType;
        $siteType->nom=$request->input('nom');
        $siteType->description=$request->input('description');
        $siteType -> save();
        return redirect('/sitetype');
    }

